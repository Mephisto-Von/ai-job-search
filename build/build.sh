#!/usr/bin/env bash
# AI Job Search - Build Standalone Executables
# This script compiles CLI tools to standalone executables

set -e

# Configuration
TOOLS=(
  "indeed-search:.agents/skills/indeed-search/cli/src/cli.ts"
  "glassdoor-search:.agents/skills/glassdoor-search/cli/src/cli.ts"
  "stepstone-search:.agents/skills/stepstone-search/cli/src/cli.ts"
  "pakistan-search:.agents/skills/pakistan-search/cli/src/cli.ts"
  "india-search:.agents/skills/india-search/cli/src/cli.ts"
)

PLATFORMS=(
  "windows:windows-x64:.exe"
  "linux:linux-x64:"
  "macos:darwin-x64:"
)

VERSION="2.0.0"
BUILD_DIR="build/dist"
OUTPUT_DIR="build/output"

echo "=== AI Job Search CLI Builder v${VERSION} ==="
echo ""

# Clean
echo "Cleaning build directories..."
rm -rf "${BUILD_DIR}" "${OUTPUT_DIR}"
mkdir -p "${BUILD_DIR}" "${OUTPUT_DIR}"

# Build functions
build_tool() {
  local name=$1
  local script=$2
  local platform=$3
  local target=$4
  local ext=$5
  
  local output="${BUILD_DIR}/${name}-${platform}${ext}"
  
  echo "Building ${name} for ${platform}..."
  if bun build --compile --target="${target}" "${script}" --outfile "${output}" 2>/dev/null; then
    echo "  ✓ ${name}-${platform}${ext}"
    return 0
  else
    echo "  ✗ Failed to build ${name} for ${platform}"
    return 1
  fi
}

# Build all tools for all platforms
build_all() {
  local success=0
  local total=0
  
  for tool_info in "${TOOLS[@]}"; do
    IFS=':' read -r name script <<< "${tool_info}"
    for platform_info in "${PLATFORMS[@]}"; do
      IFS=':' read -r platform target ext <<< "${platform_info}"
      total=$((total + 1))
      if build_tool "${name}" "${script}" "${platform}" "${target}" "${ext}"; then
        success=$((success + 1))
      fi
    done
  done
  
  echo ""
  echo "Built ${success}/${total} executables"
}

# Create Windows batch wrappers
create_windows_wrappers() {
  echo "Creating Windows batch wrappers..."
  
  for tool_info in "${TOOLS[@]}"; do IFS=':' read -r name script <<< "${tool_info}"
    local batch_file="${BUILD_DIR}/${name}.cmd"
    cat > "${batch_file}" << EOF
@echo off
REM AI Job Search - ${name} launcher
set SCRIPT_DIR=%~dp0
"%SCRIPT_DIR%${name}-windows.exe" %*
EOF
    echo "  ✓ ${name}.cmd"
  done
}

# Create NSIS installer scripts
create_nsis_installers() {
  echo "Creating NSIS installer scripts..."
  
  for tool_info in "${TOOLS[@]}"; do
    IFS=':' read -r name script <<< "${tool_info}"
    local nsis_file="${BUILD_DIR}/${name}-installer.nsi"
    
    cat > "${nsis_file}" << EOF
!include "MUI2.nsh"

Name "${name}"
OutFile "${OUTPUT_DIR}/${name}-setup-${VERSION}.exe"
InstallDir "\$PROGRAMFILES\\${name}"

!insertmacro MUI_PAGE_DIRECTORY
!insertmacro MUI_PAGE_INSTFILES
!insertmacro MUI_UNPAGE_CONFIRM
!insertmacro MUI_UNPAGE_INSTFILES

!insertmacro MUI_LANGUAGE "English"

Section "Install"
  SetOutPath \$INSTDIR
  File "${BUILD_DIR}\\${name}-windows.exe"
  CreateDirectory "\$SMPROGRAMS\\${name}"
  CreateShortCut "\$SMPROGRAMS\\${name}\\${name}.lnk" "\$INSTDIR\\${name}-windows.exe"
  CreateShortCut "\$DESKTOP\\${name}.lnk" "\$INSTDIR\\${name}-windows.exe"
  WriteUninstaller "\$INSTDIR\\uninstall.exe"
  
  ; Add to PATH
  EnVar::AddValue "PATH" "\$INSTDIR"
SectionEnd

Section "Uninstall"
  Delete "\$INSTDIR\\${name}-windows.exe"
  Delete "\$INSTDIR\\uninstall.exe"
  RMDir /r "\$SMPROGRAMS\\${name}"
  Delete "\$DESKTOP\\${name}.lnk"
  EnVar::RemoveValue "PATH" "\$INSTDIR"
  RMDir \$INSTDIR
SectionEnd
EOF
    echo "  ✓ ${name}-installer.nsi"
  done
}

# Create Inno Setup scripts
create_inno_setup() {
  echo "Creating Inno Setup scripts..."
  
  for tool_info in "${TOOLS[@]}"; do
    IFS=':' read -r name script <<< "${tool_info}"
    local iss_file="${BUILD_DIR}/${name}-installer.iss"
    
    cat > "${iss_file}" << EOF
; AI Job Search - ${name} Installer
; Generated by build script

#define MyAppName "${name}"
#define MyAppVersion "${VERSION}"
#define MyAppPublisher "AI Job Search"
#define MyAppURL "https://github.com/MadsLorentzen/ai-job-search"

[Setup]
AppId={{$(cat /proc/sys/kernel/random/uuid)}}
AppName={#MyAppName}
AppVersion={#MyAppVersion}
AppPublisher={#MyAppPublisher}
AppPublisherURL={#MyAppURL}
DefaultDirName={autopf}\\{#MyAppName}
DefaultGroupName={#MyAppName}
OutputDir=${OUTPUT_DIR}
OutputBaseFilename=${name}-setup-${VERSION}
Compression=lzma2/ultra64
SolidCompression=yes
WizardStyle=modern

[Languages]
Name: "english"; MessagesFile: "compiler:Default.isl"

[Files]
Source: "${BUILD_DIR}\\${name}-windows.exe"; DestDir: "{app}"; Flags: ignoreversion

[Icons]
Name: "{group}\\{#MyAppName}"; Filename: "{app}\\${name}-windows.exe"
Name: "{group}\\Uninstall {#MyAppName}"; Filename: "{uninstallexe}"

[Run]
Filename: "{app}\\${name}-windows.exe"; Parameters: "--help"; Description: "Test installation"; Flags: postinstall skipifsilent
EOF
    echo "  ✓ ${name}-installer.iss"
  done
}

# Create portable ZIP package
create_portable_package() {
  echo "Creating portable package..."
  
  cd "${BUILD_DIR}"
  zip -j "${OUTPUT_DIR}/ai-job-search-portable-${VERSION}.zip" \
    *.exe *.cmd *.nsi *.iss 2>/dev/null || true
  cd - > /dev/null
  
  echo "  ✓ ai-job-search-portable-${VERSION}.zip"
}

# Main
echo "Starting build..."
build_all

echo ""
echo "Creating installer scripts..."
create_windows_wrappers
create_nsis_installers
create_inno_setup

echo ""
echo "Creating portable package..."
create_portable_package

echo ""
echo "=== Build Complete ==="
echo "Output directory: ${OUTPUT_DIR}"
echo ""
echo "Files generated:"
ls -lh "${OUTPUT_DIR}"/ 2>/dev/null | tail -n +2

echo ""
echo "To create installers:"
echo "  1. Install NSIS: https://nsis.sourceforge.io/"
echo "  2. Run: makensis ${BUILD_DIR}/*-installer.nsi"
echo "  3. Or install Inno Setup and open .iss files"
