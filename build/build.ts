#!/usr/bin/env bun
// Build script for AI Job Search CLI tools
// Compiles TypeScript to standalone executables for Windows, macOS, and Linux

import { $ } from "bun"
import { mkdir, rm, cp, readdir, stat } from "fs/promises"
import { join, basename } from "path"

const BUILD_DIR = join(import.meta.dir, "dist")
const OUTPUT_DIR = join(import.meta.dir, "output")

interface BuildTarget {
  name: string
  script: string
  output: string
}

const CLI_TOOLS: BuildTarget[] = [
  { name: "indeed-search", script: ".agents/skills/indeed-search/cli/src/cli.ts", output: "indeed-search" },
  { name: "glassdoor-search", script: ".agents/skills/glassdoor-search/cli/src/cli.ts", output: "glassdoor-search" },
  { name: "stepstone-search", script: ".agents/skills/stepstone-search/cli/src/cli.ts", output: "stepstone-search" },
  { name: "pakistan-search", script: ".agents/skills/pakistan-search/cli/src/cli.ts", output: "pakistan-search" },
  { name: "india-search", script: ".agents/skills/india-search/cli/src/cli.ts", output: "india-search" },
]

interface Platform {
  name: string
  bunTarget: string
  exeExtension: string
}

const PLATFORMS: Platform[] = [
  { name: "windows", bunTarget: "windows-x64", exeExtension: ".exe" },
  { name: "linux", bunTarget: "linux-x64", exeExtension: "" },
  { name: "macos", bunTarget: "darwin-x64", exeExtension: "" },
]

async function clean() {
  console.log("Cleaning build directories...")
  await rm(BUILD_DIR, { recursive: true, force: true })
  await rm(OUTPUT_DIR, { recursive: true, force: true })
  await mkdir(BUILD_DIR, { recursive: true })
  await mkdir(OUTPUT_DIR, { recursive: true })
}

async function buildTool(tool: BuildTarget, platform: Platform): Promise<string> {
  const outputFile = join(BUILD_DIR, `${tool.output}-${platform.name}${platform.exeExtension}`)
  
  console.log(`Building ${tool.name} for ${platform.name}...`)
  
  try {
    await $`bun build --compile --target=${platform.bunTarget} ${tool.script} --outfile ${outputFile}`
    console.log(`  ✓ ${tool.name} (${platform.name})`)
    return outputFile
  } catch (e) {
    console.error(`  ✗ Failed to build ${tool.name} for ${platform.name}: ${e}`)
    return ""
  }
}

async function createWindowsExeWrapper(toolName: string): Promise<string> {
  const batchContent = `@echo off
REM AI Job Search - ${toolName} launcher
REM This wrapper forwards arguments to the Bun executable

set SCRIPT_DIR=%~dp0
"%SCRIPT_DIR%${toolName}-windows.exe" %*`

  const batchFile = join(BUILD_DIR, `${toolName}.cmd`)
  await Bun.write(batchFile, batchContent)
  return batchFile
}

